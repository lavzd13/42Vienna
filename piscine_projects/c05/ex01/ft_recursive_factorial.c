int	ft_recursive_factorial(int nb)
{
	if (nb == 1 || nb == 0)
		return (1);
	else if (nb < 0)
		return (0);
	else
		return (nb * ft_recursive_factorial(nb - 1));
}

#include<stdio.h>
int main()
{
	int a = ft_recursive_factorial(-6);
	printf("%d", a);
}

