#include <unistd.h>

void	ft_print_comb(void)
{	
	char	h;
	char	t;
	char	u;

	h = '0';
	while (h <= '7')
	{	
		t = h + 1;
		while (t <= '8')
		{	
			u = t + 1;
			while (u <= '9')
			{
				write(1, &h, 1);
				write(1, &t, 1);
				write(1, &u, 1);
				if (h != '7')
					write(1, ", ", 2);
				u++;
			}
			t++;
		}
		h++;
	}
}
